package intermediate_java_programs;

public class power {
    public static void main(String[] args) {
        double a = 30;
        double b = 2;
        System.out.println(Math.pow(a, b));
        a = 3;
        b = 4;
        System.out.println(Math.pow(a, b));

        a = 2;
        b = 6;
        System.out.println(Math.pow(a, b));
    }
}
